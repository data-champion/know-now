//! dbt project generator crate for know-now.
//!
//! Produces a complete dbt project: `dbt_project.yml`, `sources.yml`,
//! staging models, mart models, and `schema.yml` for each layer.

use know_now_codegen::artifact::{ArtifactDescriptor, ArtifactKind};
use know_now_codegen::generator::{GenerationError, Generator};
use know_now_contract::contract::{
    ContractAttribute, ContractEntity, ContractRelationship, ContractSourceSystem, GeneratorContract,
};

const GENERATOR_NAME: &str = "know_now_gen_dbt";

pub struct DbtGenerator {
    version: String,
}

impl DbtGenerator {
    #[must_use]
    pub fn new() -> Self {
        Self {
            version: env!("CARGO_PKG_VERSION").to_owned(),
        }
    }
}

impl Default for DbtGenerator {
    fn default() -> Self {
        Self::new()
    }
}

impl Generator for DbtGenerator {
    fn name(&self) -> &str {
        GENERATOR_NAME
    }

    fn version(&self) -> &str {
        &self.version
    }

    fn generate(
        &self,
        contract: &GeneratorContract,
    ) -> Result<Vec<ArtifactDescriptor>, Vec<GenerationError>> {
        if contract.entities.is_empty() {
            return Ok(vec![]);
        }

        let mut artifacts = Vec::new();
        let project_name = contract
            .project
            .as_ref()
            .map_or("know_now_project", |p| p.name.as_str());

        artifacts.push(emit_dbt_project_yml(project_name, &self.version));

        if !contract.source_systems.is_empty() {
            artifacts.push(emit_sources_yml(
                &contract.source_systems,
                &self.version,
            ));

            for source in &contract.source_systems {
                for table in &source.tables {
                    artifacts.push(emit_staging_model(source, table, &self.version));
                }
            }

            artifacts.push(emit_staging_schema_yml(
                &contract.source_systems,
                &self.version,
            ));
        }

        for entity in &contract.entities {
            artifacts.push(emit_mart_model(entity, &contract.relationships, &self.version));
        }

        artifacts.push(emit_marts_schema_yml(
            &contract.entities,
            &self.version,
        ));

        artifacts.sort_by(|a, b| a.path.cmp(&b.path));
        Ok(artifacts)
    }
}

fn ownership_comment(artifact_id: &str, version: &str) -> String {
    format!("-- Generated by know-now. Artifact ID: {artifact_id}. Generator: {GENERATOR_NAME}. Version: {version}. Do not edit directly unless you intend to fork this artifact.")
}

fn yaml_ownership_comment(artifact_id: &str, version: &str) -> String {
    format!("# Generated by know-now. Artifact ID: {artifact_id}. Generator: {GENERATOR_NAME}. Version: {version}. Do not edit directly unless you intend to fork this artifact.")
}

fn emit_dbt_project_yml(project_name: &str, version: &str) -> ArtifactDescriptor {
    let safe_name = project_name
        .chars()
        .map(|c| if c.is_alphanumeric() || c == '_' { c } else { '_' })
        .collect::<String>();

    let content = format!(
        "{comment}

name: '{safe_name}'
version: '1.0.0'
config-version: 2
profile: '{safe_name}'

model-paths:
  - models
  - ../../../custom/dbt/models

macro-paths:
  - ../../../custom/dbt/macros

seed-paths:
  - ../../../custom/dbt/seeds

test-paths:
  - tests

clean-targets:
  - target
  - dbt_packages
",
        comment = yaml_ownership_comment("art_dbt_project_yml", version),
    );

    ArtifactDescriptor {
        path: "dbt/dbt_project.yml".into(),
        kind: ArtifactKind::DbtModel,
        artifact_id: "art_dbt_project_yml".into(),
        generator: GENERATOR_NAME.into(),
        generator_version: version.to_owned(),
        content,
        metadata_object_ids: vec![],
    }
}

fn emit_sources_yml(
    sources: &[ContractSourceSystem],
    version: &str,
) -> ArtifactDescriptor {
    let mut lines = vec![
        yaml_ownership_comment("art_dbt_sources", version),
        String::new(),
        "version: 2".to_owned(),
        String::new(),
        "sources:".to_owned(),
    ];

    let mut sorted_sources: Vec<&ContractSourceSystem> = sources.iter().collect();
    sorted_sources.sort_by_key(|s| &s.name);

    for source in sorted_sources {
        lines.push(format!("  - name: {}", source.name));
        if let Some(ref desc) = source.description {
            lines.push(format!("    description: \"{}\"", escape_yaml(desc)));
        }
        if let Some(schema) = source.tables.first().and_then(|t| t.schema.as_ref()) {
            lines.push(format!("    schema: {schema}"));
        }
        if !source.tables.is_empty() {
            lines.push("    tables:".to_owned());
            let mut sorted_tables = source.tables.clone();
            sorted_tables.sort_by(|a, b| a.name.cmp(&b.name));
            for table in &sorted_tables {
                lines.push(format!("      - name: {}", table.name));
                if let Some(ref entity) = table.entity {
                    lines.push(format!(
                        "        description: \"Source table for entity {entity}.\""
                    ));
                }
                if !table.columns.is_empty() {
                    lines.push("        columns:".to_owned());
                    for col in &table.columns {
                        lines.push(format!("          - name: {}", col.source));
                        if col.source != col.target {
                            lines.push(format!(
                                "            description: \"Maps to {}.\"",
                                col.target
                            ));
                        }
                    }
                }
            }
        }
    }

    lines.push(String::new());

    let ids: Vec<String> = sources
        .iter()
        .flat_map(|s| s.tables.iter().filter_map(|t| t.entity.clone()))
        .collect();

    ArtifactDescriptor {
        path: "dbt/models/sources.yml".into(),
        kind: ArtifactKind::DbtSchema,
        artifact_id: "art_dbt_sources".into(),
        generator: GENERATOR_NAME.into(),
        generator_version: version.to_owned(),
        content: lines.join("\n"),
        metadata_object_ids: ids,
    }
}

fn emit_staging_model(
    source: &ContractSourceSystem,
    table: &know_now_contract::contract::ContractSourceTable,
    version: &str,
) -> ArtifactDescriptor {
    let model_name = format!("stg_{}_{}", source.name, table.name);
    let mut lines = Vec::new();

    lines.push(ownership_comment(&format!("art_dbt_{model_name}"), version));
    lines.push(String::new());

    lines.push("with source as (".to_owned());
    lines.push(String::new());
    lines.push(format!(
        "    select * from {{{{ source('{}', '{}') }}}}",
        source.name, table.name
    ));
    lines.push(String::new());
    lines.push(")".to_owned());
    lines.push(String::new());
    lines.push("select".to_owned());

    if table.columns.is_empty() {
        lines.push("    *".to_owned());
    } else {
        let last_idx = table.columns.len() - 1;
        for (idx, col) in table.columns.iter().enumerate() {
            let trailing = if idx < last_idx { "," } else { "" };
            if col.source == col.target || col.transform.is_none() {
                lines.push(format!("    {}{trailing}", col.source));
            } else if let Some(ref transform) = col.transform {
                lines.push(format!(
                    "    {transform} as {}{trailing}",
                    col.target
                ));
            }
        }
    }

    lines.push(String::new());
    lines.push("from source".to_owned());
    lines.push(String::new());

    let entity_id = table.entity.clone().unwrap_or_default();

    ArtifactDescriptor {
        path: format!("dbt/models/staging/{model_name}.sql"),
        kind: ArtifactKind::DbtModel,
        artifact_id: format!("art_dbt_{model_name}"),
        generator: GENERATOR_NAME.into(),
        generator_version: version.to_owned(),
        content: lines.join("\n"),
        metadata_object_ids: vec![entity_id],
    }
}

fn emit_staging_schema_yml(
    sources: &[ContractSourceSystem],
    version: &str,
) -> ArtifactDescriptor {
    let mut lines = vec![
        yaml_ownership_comment("art_dbt_staging_schema", version),
        String::new(),
        "version: 2".to_owned(),
        String::new(),
        "models:".to_owned(),
    ];

    let mut sorted_sources: Vec<&ContractSourceSystem> = sources.iter().collect();
    sorted_sources.sort_by_key(|s| &s.name);

    for source in sorted_sources {
        let mut sorted_tables = source.tables.clone();
        sorted_tables.sort_by(|a, b| a.name.cmp(&b.name));
        for table in &sorted_tables {
            let model_name = format!("stg_{}_{}", source.name, table.name);
            lines.push(format!("  - name: {model_name}"));
            if let Some(ref entity) = table.entity {
                lines.push(format!(
                    "    description: \"Staging model for entity {entity} from source {source_name}/{table_name}.\"",
                    source_name = source.name,
                    table_name = table.name,
                ));
            }
            if !table.columns.is_empty() {
                lines.push("    columns:".to_owned());
                for col in &table.columns {
                    lines.push(format!("      - name: {}", col.target));
                }
            }
        }
    }

    lines.push(String::new());

    ArtifactDescriptor {
        path: "dbt/models/staging/schema.yml".into(),
        kind: ArtifactKind::DbtSchema,
        artifact_id: "art_dbt_staging_schema".into(),
        generator: GENERATOR_NAME.into(),
        generator_version: version.to_owned(),
        content: lines.join("\n"),
        metadata_object_ids: vec![],
    }
}

fn emit_mart_model(
    entity: &ContractEntity,
    relationships: &[ContractRelationship],
    version: &str,
) -> ArtifactDescriptor {
    let mut lines = Vec::new();
    lines.push(ownership_comment(
        &format!("art_dbt_mart_{}", entity.name),
        version,
    ));
    lines.push(String::new());

    #[allow(clippy::suspicious_operation_groupings)]
    let incoming_joins: Vec<&ContractRelationship> = relationships
        .iter()
        .filter(|r| r.from_entity == entity.name && r.from_entity != r.to_entity)
        .collect();

    lines.push("select".to_owned());

    let attr_count = entity.attributes.len();
    for (idx, attr) in entity.attributes.iter().enumerate() {
        let trailing = if idx < attr_count - 1 || !incoming_joins.is_empty() {
            ","
        } else {
            ""
        };
        lines.push(format!("    base.{}{trailing}", attr.name));
    }

    for (idx, join) in incoming_joins.iter().enumerate() {
        let trailing = if idx < incoming_joins.len() - 1 { "," } else { "" };
        let alias = &join.to_entity;
        let to_key = join.to_key.as_deref().unwrap_or("id");
        lines.push(format!("    {alias}.{to_key} as {alias}_{to_key}{trailing}"));
    }

    lines.push(String::new());
    lines.push(format!("from {{{{ ref('{}') }}}} as base", entity.name));

    for join in &incoming_joins {
        let from_key = join.from_key.as_deref().unwrap_or("id");
        let to_key = join.to_key.as_deref().unwrap_or("id");
        lines.push(format!(
            "left join {{{{ ref('{}') }}}} as {} on base.{from_key} = {}.{to_key}",
            join.to_entity, join.to_entity, join.to_entity
        ));
    }

    lines.push(String::new());

    ArtifactDescriptor {
        path: format!("dbt/models/marts/{}.sql", entity.name),
        kind: ArtifactKind::DbtModel,
        artifact_id: format!("art_dbt_mart_{}", entity.name),
        generator: GENERATOR_NAME.into(),
        generator_version: version.to_owned(),
        content: lines.join("\n"),
        metadata_object_ids: vec![entity.id.clone()],
    }
}

fn emit_marts_schema_yml(
    entities: &[ContractEntity],
    version: &str,
) -> ArtifactDescriptor {
    let mut lines = vec![
        yaml_ownership_comment("art_dbt_marts_schema", version),
        String::new(),
        "version: 2".to_owned(),
        String::new(),
        "models:".to_owned(),
    ];

    let mut sorted: Vec<&ContractEntity> = entities.iter().collect();
    sorted.sort_by_key(|e| &e.name);

    for entity in sorted {
        lines.push(format!("  - name: {}", entity.name));
        if let Some(ref desc) = entity.description {
            lines.push(format!("    description: \"{}\"", escape_yaml(desc)));
        }
        if !entity.attributes.is_empty() {
            lines.push("    columns:".to_owned());
            for attr in &entity.attributes {
                emit_column_entry(attr, &mut lines);
            }
        }
    }

    lines.push(String::new());

    let ids: Vec<String> = entities.iter().map(|e| e.id.clone()).collect();

    ArtifactDescriptor {
        path: "dbt/models/marts/schema.yml".into(),
        kind: ArtifactKind::DbtSchema,
        artifact_id: "art_dbt_marts_schema".into(),
        generator: GENERATOR_NAME.into(),
        generator_version: version.to_owned(),
        content: lines.join("\n"),
        metadata_object_ids: ids,
    }
}

fn emit_column_entry(attr: &ContractAttribute, lines: &mut Vec<String>) {
    lines.push(format!("      - name: {}", attr.name));
    if let Some(ref desc) = attr.description {
        lines.push(format!("        description: \"{}\"", escape_yaml(desc)));
    }
    let mut tests = Vec::new();
    if attr.required == Some(true) {
        tests.push("not_null");
    }
    if attr.is_unique == Some(true) {
        tests.push("unique");
    }
    if !tests.is_empty() {
        lines.push("        tests:".to_owned());
        for test in tests {
            lines.push(format!("          - {test}"));
        }
    }
}

fn escape_yaml(s: &str) -> String {
    s.replace('\\', "\\\\").replace('"', "\\\"")
}

