//! Deterministic synthetic fixture data generator for know-now.
//!
//! Produces CSV files with obviously-synthetic rows that respect
//! logical types, semantic types, required fields, and FK relationships.
//! Uses a seeded PRNG — no system clock, no entropy source.

use std::collections::BTreeMap;

use know_now_codegen::artifact::{ArtifactDescriptor, ArtifactKind};
use know_now_codegen::generator::{GenerationError, Generator};
use know_now_contract::contract::{
    ContractAttribute, ContractEntity, ContractRelationship, GeneratorContract,
};

mod prng;
mod values;

use prng::SeededRng;

const GENERATOR_NAME: &str = "know_now_gen_fixtures";
const DEFAULT_ROW_COUNT: usize = 10;
const DEFAULT_SEED: u64 = 0x6b6e_6f77_6e6f_7721;

pub struct FixtureGenerator {
    version: String,
    seed: u64,
    row_count: usize,
}

impl FixtureGenerator {
    #[must_use]
    pub fn new() -> Self {
        Self {
            version: env!("CARGO_PKG_VERSION").to_owned(),
            seed: DEFAULT_SEED,
            row_count: DEFAULT_ROW_COUNT,
        }
    }

    #[must_use]
    pub fn with_seed(mut self, seed: u64) -> Self {
        self.seed = seed;
        self
    }

    #[must_use]
    pub fn with_row_count(mut self, count: usize) -> Self {
        self.row_count = count;
        self
    }
}

impl Default for FixtureGenerator {
    fn default() -> Self {
        Self::new()
    }
}

impl Generator for FixtureGenerator {
    fn name(&self) -> &str {
        GENERATOR_NAME
    }

    fn version(&self) -> &str {
        &self.version
    }

    #[allow(clippy::too_many_lines)]
    fn generate(
        &self,
        contract: &GeneratorContract,
    ) -> Result<Vec<ArtifactDescriptor>, Vec<GenerationError>> {
        if contract.entities.is_empty() {
            return Ok(vec![]);
        }

        let mut rng = SeededRng::new(self.seed);
        let mut artifacts = Vec::new();

        let generation_order = topological_sort(&contract.entities, &contract.relationships);
        let mut pk_pools: BTreeMap<String, Vec<String>> = BTreeMap::new();

        for entity_name in &generation_order {
            let entity = contract
                .entities
                .iter()
                .find(|e| &e.name == entity_name)
                .unwrap();

            let csv = generate_entity_csv(
                entity,
                &contract.relationships,
                &pk_pools,
                &mut rng,
                self.row_count,
            );

            let pk_values: Vec<String> = extract_pk_values(entity, &csv);
            pk_pools.insert(entity.name.clone(), pk_values);

            artifacts.push(ArtifactDescriptor {
                path: format!("fixtures/{}.csv", entity.name),
                kind: ArtifactKind::MarkdownDoc,
                artifact_id: format!("art_fixture_{}", entity.name),
                generator: GENERATOR_NAME.into(),
                generator_version: self.version.clone(),
                content: csv,
                metadata_object_ids: vec![entity.id.clone()],
            });
        }

        let readme = generate_readme(&generation_order, self.seed, self.row_count, &self.version);
        artifacts.push(ArtifactDescriptor {
            path: "fixtures/README.md".into(),
            kind: ArtifactKind::MarkdownDoc,
            artifact_id: "art_fixture_readme".into(),
            generator: GENERATOR_NAME.into(),
            generator_version: self.version.clone(),
            content: readme,
            metadata_object_ids: vec![],
        });

        artifacts.sort_by(|a, b| a.path.cmp(&b.path));
        Ok(artifacts)
    }
}

fn generate_entity_csv(
    entity: &ContractEntity,
    relationships: &[ContractRelationship],
    pk_pools: &BTreeMap<String, Vec<String>>,
    rng: &mut SeededRng,
    row_count: usize,
) -> String {
    let mut lines = Vec::new();

    lines.push(format!(
        "# SYNTHETIC DATA — do not use in production. Generated by {GENERATOR_NAME}."
    ));

    let header: Vec<&str> = entity.attributes.iter().map(|a| a.name.as_str()).collect();
    lines.push(header.join(","));

    for row_idx in 0..row_count {
        let row: Vec<String> = entity
            .attributes
            .iter()
            .map(|attr| {
                generate_cell(attr, entity, relationships, pk_pools, rng, row_idx)
            })
            .collect();
        lines.push(row.join(","));
    }

    lines.push(String::new());
    lines.join("\n")
}

fn generate_cell(
    attr: &ContractAttribute,
    entity: &ContractEntity,
    relationships: &[ContractRelationship],
    pk_pools: &BTreeMap<String, Vec<String>>,
    rng: &mut SeededRng,
    row_idx: usize,
) -> String {
    let is_required = attr.required.unwrap_or(false);
    if !is_required && rng.next_u64().is_multiple_of(10) {
        return String::new();
    }

    if let Some(fk_pool) = find_fk_pool(attr, entity, relationships, pk_pools) {
        if !fk_pool.is_empty() {
            let idx = rng.next_u64() as usize % fk_pool.len();
            return fk_pool[idx].clone();
        }
    }

    if entity.business_key.contains(&attr.name) || is_pk_like(attr) {
        return values::generate_pk(attr, rng, row_idx);
    }

    if let Some(ref semantic) = attr.semantic_type {
        return values::generate_semantic(semantic, rng, row_idx);
    }

    let logical = attr.logical_type.as_deref().unwrap_or("string");
    values::generate_logical(logical, rng, row_idx)
}

fn find_fk_pool<'a>(
    attr: &ContractAttribute,
    entity: &ContractEntity,
    relationships: &[ContractRelationship],
    pk_pools: &'a BTreeMap<String, Vec<String>>,
) -> Option<&'a Vec<String>> {
    for rel in relationships {
        if rel.from_entity == entity.name && rel.from_key.as_deref() == Some(&attr.name) {
            return pk_pools.get(&rel.to_entity);
        }
    }
    None
}

fn is_pk_like(attr: &ContractAttribute) -> bool {
    attr.is_unique == Some(true) && attr.required == Some(true)
}

fn extract_pk_values(entity: &ContractEntity, csv: &str) -> Vec<String> {
    let pk_attr = entity.business_key.first().or_else(|| {
        entity
            .attributes
            .iter()
            .find(|a| is_pk_like(a))
            .map(|a| &a.name)
    });

    let Some(pk_name) = pk_attr else {
        return vec![];
    };

    let mut lines = csv.lines().filter(|l| !l.starts_with('#'));
    let Some(header_line) = lines.next() else {
        return vec![];
    };

    let headers: Vec<&str> = header_line.split(',').collect();
    let Some(pk_idx) = headers.iter().position(|h| h == pk_name) else {
        return vec![];
    };

    lines
        .filter(|l| !l.is_empty())
        .filter_map(|line| {
            let fields: Vec<&str> = line.split(',').collect();
            fields.get(pk_idx).map(|v| (*v).to_owned())
        })
        .collect()
}

fn topological_sort(
    entities: &[ContractEntity],
    relationships: &[ContractRelationship],
) -> Vec<String> {
    let entity_names: Vec<String> = entities.iter().map(|e| e.name.clone()).collect();
    let mut deps: BTreeMap<String, Vec<String>> = BTreeMap::new();
    for name in &entity_names {
        deps.insert(name.clone(), vec![]);
    }

    for rel in relationships {
        if rel.from_entity != rel.to_entity {
            if let Some(dep_list) = deps.get_mut(&rel.from_entity) {
                if entity_names.contains(&rel.to_entity) {
                    dep_list.push(rel.to_entity.clone());
                }
            }
        }
    }

    let visited: BTreeMap<String, bool> = BTreeMap::new();
    let mut result = Vec::new();

    for name in &entity_names {
        topo_visit(name, &deps, &visited, &mut result);
    }

    result
}

fn topo_visit(
    name: &str,
    deps: &BTreeMap<String, Vec<String>>,
    visited: &BTreeMap<String, bool>,
    result: &mut Vec<String>,
) {
    if visited.contains_key(name) {
        return;
    }
    let mut visited = visited.clone();
    visited.insert(name.to_owned(), true);

    if let Some(dep_list) = deps.get(name) {
        for dep in dep_list {
            topo_visit(dep, deps, &visited, result);
        }
    }

    if !result.contains(&name.to_owned()) {
        result.push(name.to_owned());
    }
}

fn generate_readme(
    entities: &[String],
    seed: u64,
    row_count: usize,
    version: &str,
) -> String {
    let mut lines = Vec::new();
    lines.push(format!(
        "<!-- Generated by know-now. Generator: {GENERATOR_NAME}. Version: {version}. Do not edit directly unless you intend to fork this artifact. -->"
    ));
    lines.push(String::new());
    lines.push("# Synthetic Fixture Data".to_owned());
    lines.push(String::new());
    lines.push(
        "**This data is synthetic and must not be used in production.**".to_owned(),
    );
    lines.push(String::new());
    lines.push(format!("- Seed: `{seed:#018x}`"));
    lines.push(format!("- Rows per entity: {row_count}"));
    lines.push(format!("- Generator: `{GENERATOR_NAME}` v{version}"));
    lines.push(String::new());
    lines.push("## Files".to_owned());
    lines.push(String::new());
    for entity in entities {
        lines.push(format!("- `{entity}.csv`"));
    }
    lines.push(String::new());

    lines.join("\n")
}

