    #[test]
    fn fk_values_reference_parent_pk() {
        let gen = FixtureGenerator::new().with_row_count(5);
        let artifacts = gen.generate(&contract_with_fk()).unwrap();

        let customer_csv = &artifacts
            .iter()
            .find(|a| a.path == "fixtures/customer.csv")
            .unwrap()
            .content;
        let customer_ids: Vec<String> = customer_csv
            .lines()
            .filter(|l| !l.starts_with('#') && !l.is_empty())
            .skip(1) // header
            .filter_map(|line| line.split(',').next().map(String::from))
            .collect();

        let order_csv = &artifacts
            .iter()
            .find(|a| a.path == "fixtures/order.csv")
            .unwrap()
            .content;
        let order_customer_ids: Vec<String> = order_csv
            .lines()
            .filter(|l| !l.starts_with('#') && !l.is_empty())
            .skip(1)
            .filter_map(|line| line.split(',').nth(1).map(String::from))
            .collect();

        for fk_val in &order_customer_ids {
            assert!(
                customer_ids.contains(fk_val),
                "FK value '{fk_val}' not found in parent PK pool {customer_ids:?}"
            );
        }
    }

